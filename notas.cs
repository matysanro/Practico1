flujo secuencial del juego
-------------------------------

menú inicial
mostrar opciones: jugar, salir.
mostrar una lista de mapas disponibles.
elegir mapa
cargar mapa
cargar recursos necesarios: texturas, modelos, sonidos.
seleccionar equipo
asignar jugadores a cada equipo.
spawn de jugadores
asignar arma inicial al jugador.
establecer estado inicial: vida completa, munición llena, etc.
mostrar cuenta regresiva de fin del mapa
leer entradas del usuario. (movimiento, disparos, colisiones)
actualizar posición de los jugadores.
detectar colisiones y calcular daño.
verificar si hay un equipo ganador o condiciones de fin.
mostrar resultado: ganador, estadísticas, etc.

